#include "libft.h"

static int	ft_isset(char c, char const *set)
{
	int	i;

	i = 0;
	while (set[i])
	{
		if (c == set[i])
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	int		start;
	int		end;
	size_t	size;

	start = 0;
	while (s1[start] && ft_isset(s1[start], set))
		start++;
	end = ft_strlen(s1) - 1;
	while (end > start && ft_isset(s1[end], set))
		end--;
	size = end - start + 2;
	str = malloc(sizeof(char) * (size));
	if (!str)
		return (NULL);
	ft_strlcpy(str, s1 + start, size);
	return (str);
}
